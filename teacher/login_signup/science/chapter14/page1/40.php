Environmental Conservation
We only have one planet on which we live. The planet is full of resources, some renewable and some not, but unfortunately, we have an ever-growing population that poses a huge threat to all these resources. It is therefore unclear what future generations will find, if ever, and what life will be like for them if we exploit all the current resources. To fix this issue as well as ensure we leave some resources for the future, environmental conservation comes in and this article will delve more into the topic.

