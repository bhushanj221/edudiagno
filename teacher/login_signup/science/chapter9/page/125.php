An addition reaction, in organic chemistry, is in its simplest terms an organic reaction where two or more
molecules combine to form a larger one (the adduct).
<br>
Addition reactions are limited to chemical compounds that have multiple bonds,
such as molecules with carbon–carbon double bonds (alkenes), or with triple bonds (alkynes),
and compounds that have rings, which are also considered points of unsaturation. Molecules containing
carbon—hetero double bonds like carbonyl (C=O) groups, or imine (C=N) groups, can undergo addition,
as they too have double-bond character.
<br>
An addition reaction is the reverse of an elimination reaction.
<br>
For instance, the hydration of an alkene to an alcohol is reversed by dehydration.
<br>
There are two main types of polar addition reactions: electrophilic addition and nucleophilic addition.
Two non-polar addition reactions exist as well, called free-radical addition and cycloadditions.
