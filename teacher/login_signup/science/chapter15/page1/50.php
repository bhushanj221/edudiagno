  <p >Three mutual conversions of energy forms occur at nuclear power plants: nuclear energy is converted into thermal energy, thermal energy is converted into mechanical energy, and mechanical energy is converted into electric energy.
<br><br>
These conversions occurs as follows: The heart of a nuclear power plant is the reactor, a structurally allocated area to which nuclear fuel is loaded and where the controlled chain reaction occurs. Uranium-235 is fissioned by slow (thermal) neutrons, as a result of which a tremendous amount of heat is released. This heat is removed from the reactor core by means of the coolant, which is a liquid or gaseous medium passing through the core. In most cases water is used as the coolant; however, in fast neutron reactors molten metals are used instead (e.g., molten sodium in BN-600 reactors). This is how the most difficult conversion (nuclear into thermal energy) is carried out.
<br><br>
The heat collected by the coolant from the reactor core is used to generate steam in the steam generator. The mechanical energy of steam generated in the steam generator is directed to the turbine generator, where it is converted into electric energy and is then supplied to the grid and finally to the customers. This is how the second and third conversions occur. The steam is then cooled and the condensed water is returned back to the reactor for reuse.</p>





