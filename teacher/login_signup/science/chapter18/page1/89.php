All humans start out as only one cell. This cell is called a zygote, or a fertilized egg. The zygote divides into two cells, then four cells, and so on. Eventually, the cells begin to differentiate, taking on a certain function in a part of the body. This process is called differentiation.<br>

Stem cells are cells that haven’t differentiated yet. They have the ability to divide and make an indefinite number of copies of themselves. Other cells in the body can only replicate a limited number of times before they begin to break down. When a stem cell divides, it can either remain a stem cell or turn into a differentiated cell, such as a muscle cell or a red blood cell.<br>
