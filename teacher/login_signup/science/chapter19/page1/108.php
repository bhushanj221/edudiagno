><b>Question 2:</b><br>
Answer the following.<br>
a. Which factors affect the social health?<br>
b. Which changes occur in persons continuously using internet and mobile phones?<br>
c. Which problems do the common man faces due to incidences of cyber crime?<br>
d. Explain the importance of good communication with others.<br>
<br>
<p style="color:green;"><b>ANSWER:</b></p><br><br>
a. The factors which affect social health are:<br>
<br>
<b>Mental stress:</b> The response of our body to a demand or threat is known as stress and it can be both physical and mental. Mental stress is a major contributor to poor social health. Increase in competition in several aspects of life has resulted in increased mental stress. Stressors like best education, better employment, hectic work schedules, nuclear families, loneliness, etc. have added to this menace. <br>
<b>Addiction: </b>Addiction is a mental condition in which an individual's brain is manifested by compulsive use of harmful substances despite being aware of the fact that those substances are harmful.<br>
Things such as alcohol, drugs and tobacco are addictive substances, frequent use of which results to their addiction. The effect of these substances are long lasting on mental, physical and social health. Continuous use of such substances adversely affects the nervous system, thus impairing the thinking and decision making ability of the individual. It also leads to poor social health which affects the interpersonal relationships and the social status of an individual.<br>
Incurable diseases: Diseases like AIDS, tuberculosis etc. are considered incurable diseases and are considered a deterrent to social health. Apart from being deadly and at times being incurable, there are various kinds of social stigmas associated with these diseases. People who suffer from such diseases are often excluded from the society and forced to live a life of seclusion.<br>
<br>
b. There are various physical and mental changes which occur in a person who continuously uses internet and mobile phones. Some of the physical and mental changes are:<br>
<br>
Physical changes:<br>
 <br>

tiredness and headache<br>
insomnia<br>
forgetfulness<br>
tinnitus and joint pain<br>
problems in vision <br>
weight gain<br> 
<br>
Mental and emotional changes:<br>
the person becomes irritant<br>
lack of patience<br>
development of aggression<br>
the person becomes introvert and self centered<br>
<br>
c. Cyber crime is a crime which uses the computer for carrying out illegal actions, such as hacking data, misuse/illegal sale of literature, etc. There are various effects which cyber crime has on the mental social health of an individual. It can lead to monetory loss, leak of data and personal information of individuals and organisations etc. Such kind of actions leaves a person traumatised<br>
and disturbs the mental peace of an idivisual. This mental disturbance is reciprocated in the form physical and social effects which directly or indirectly affect the interpersonal relationship of individuals.<br>
<br>
d. Communication is a vital skill which plays an important role from personal to professional life. Good communication skills does not only mean good verbal skills but it also implies to one's listening skills. Communication with others helps to overcome your problems, provide you with moral support and also you get to understand the perspective of people on different issues. Good communication skills help people to gain confidence and overcome stress situations. Apart from being a good orator, it is important that you are a good listener too. One should listen patiently to people around them and help them in best possible way. Good communication skills help you to make friends with long lasting relationship. It also teaches you how to put forward your points without hurting the sentiments of others. In other words we can say that, good communication skill result in the all round development of an individual.<br>
