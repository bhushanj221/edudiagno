What type of changes occurs in a home having chronically ill old person? How will you help to maintain good atmosphere?<br>
<br>
<p style="color:green;"><b>ANSWER:</b></p><br><br>
Having chronically ill old patients in a family affects the physical and mental state of others members of the family as well. It is not easy to handle old age people and if they are ill, it becomes all the more difficult. It not only affects the family monetarily but also drains them physically and emotionally as it is not easy to see the suffering of your loved ones. Since, it is a tough situation, it is important that each and every person of the family keeps patience and stays calm. They need to remain positive and ward off negative feelings and thoughts.
They should try to talk to the patient and share their entire day with them, even if they are not in a state of response. Just being present around them, protects them from the feeling of loneliness.<br>
