<b>Question 6:</b><br>
What will you do? Why?<br>
a. You are spending more time in internet/mobile games, phone, etc.<br>
b. Child of your neighbor is addicted to tobacco chewing.<br>
c. Your sister has become incommunicative. She prefers to remain alone.<br>
d. You have to use free space around your home for good purpose.<br>
e. Your friend has developed the hobby of snapping selfies.<br>
f. Your brother studying in XII has developed the stress.<br>
