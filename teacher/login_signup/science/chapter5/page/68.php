  <p >In thermodynamics, the specific heat capacity (symbol cp) of a substance is the heat capacity of a sample of the substance divided by the mass of the sample. Informally, it is the amount of energy that must be added, in the form of heat, to one unit of mass of the substance in order to cause an increase of one unit in temperature. The SI unit of specific heat is joule per kelvin and kilogram, J/(K kg).For example, at a temperature of 25 °C the heat required to raise the temperature of 1 kg of water by 1 K is 4179.6 joules so the specific heat capacity of water is 4179.6 J·kg−1·K−1.

The specific heat capacity often varies with temperature, and is different for each state of matter. Liquid water has one of the highest specific heats among common substances, about 4182 J/(K kg) at 20 °C; but that of ice just below 0 °C is only 2093 J/(K kg). The specific heats of iron, granite, and hydrogen gas are about 449, 790, and 14300 J/(K kg), respectively.While the substance is undergoing a phase transition, such as melting or boiling, its specific heat is technically infinite, because the heat goes into changing its state rather than raising its temperature.</p>





